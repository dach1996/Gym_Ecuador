        INSERT INTO [CORE].[SEGUIMIENTO_PROCESOS_MEDIDAS] ([SPR_ID], [PAF_ID], [SPM_VALOR])
        SELECT spr.[SPR_ID], paf.[PAF_ID], spr.[SPR_MEDIDA_CINTURA]
        FROM [CORE].[SEGUIMIENTO_PROCESOS] spr
        INNER JOIN [CORE].[PARAMETROS_FISICOS] paf ON paf.[PAF_CODIGO] = 'MEDIDA_CINTURA'
        WHERE spr.[SPR_MEDIDA_CINTURA] IS NOT NULL
          AND NOT EXISTS (
            SELECT 1 FROM [CORE].[SEGUIMIENTO_PROCESOS_MEDIDAS] spm
            WHERE spm.[SPR_ID] = spr.[SPR_ID] AND spm.[PAF_ID] = paf.[PAF_ID]
        );

        INSERT INTO [CORE].[SEGUIMIENTO_PROCESOS_MEDIDAS] ([SPR_ID], [PAF_ID], [SPM_VALOR])
        SELECT spr.[SPR_ID], paf.[PAF_ID], spr.[SPR_MEDIDA_CADERA]
        FROM [CORE].[SEGUIMIENTO_PROCESOS] spr
        INNER JOIN [CORE].[PARAMETROS_FISICOS] paf ON paf.[PAF_CODIGO] = 'MEDIDA_CADERA'
        WHERE spr.[SPR_MEDIDA_CADERA] IS NOT NULL
          AND NOT EXISTS (
            SELECT 1 FROM [CORE].[SEGUIMIENTO_PROCESOS_MEDIDAS] spm
            WHERE spm.[SPR_ID] = spr.[SPR_ID] AND spm.[PAF_ID] = paf.[PAF_ID]
        );

